// 确定性随机
function mulberry32(seed) { return function(){let t=(seed+=0x6D2B79F5);t=Math.imul(t^(t>>>15),t|1);t^=t+Math.imul(t^(t>>>7),t|61);return((t^(t>>>14))>>>0)/4294967296;}; }
function strSeed(s){let h=2166136261;for(let i=0;i<s.length;i++){h^=s.charCodeAt(i);h=Math.imul(h,16777619);}return h>>>0;}

// ---------- 生成 ----------
function build() {
  const provinces = {};
  for (const code in BIZ_PROVINCES) {
    provinces[code] = { name: BIZ_PROVINCES[code], schools: [] };
  }

  // 1. 归类 985/211（全国招生，按 PROV_OF 归到所在省）
  const all211 = [...LIST_985.map(n => ({name:n, tier:'985'})), ...[...SET_211].map(n => ({name:n, tier:'211'}))];
  for (const s of all211) {
    const prov = PROV_OF[s.name];
    if (prov && provinces[prov]) provinces[prov].schools.push({ name: s.name, tier: s.tier, dept: guessDept(s.name) });
    else {
      // 未标省份的 985/211（如国防科大）→ 放到每个省（全国招生校）
      for (const code in provinces) {
        if (!provinces[code].schools.some(x => x.name === s.name)) {
          provinces[code].schools.push({ name: s.name, tier: s.tier, dept: guessDept(s.name) });
        }
      }
    }
  }

  // 2. 加各省本地本科 + 通用本科
  for (const code in provinces) {
    const rng = mulberry32(strSeed(code + 'benke'));
    // 本地本科
    (LOCAL_BENKE[code] || []).forEach(([name, base]) => {
      if (!provinces[code].schools.some(s => s.name === name)) {
        provinces[code].schools.push({ name, tier: guessBenkeTier(name, base), dept: guessDept(name) });
      }
    });
    // 通用本科（每省都加）
    COMMON_BENKE.forEach(([name, base]) => {
      if (!provinces[code].schools.some(s => s.name === name)) {
        provinces[code].schools.push({ name, tier: guessBenkeTier(name, base), dept: guessDept(name) });
      }
    });
  }

  // 3. 加各省专科
  for (const code in provinces) {
    (LOCAL_ZHUANKE[code] || []).forEach(([name]) => {
      if (!provinces[code].schools.some(s => s.name === name)) {
        provinces[code].schools.push({ name, tier: '3', dept: guessDept(name) });
      }
    });
  }

  // 统计 + 输出
  let total = 0;
  const stats = {};
  for (const code in provinces) {
    stats[code] = { name: provinces[code].name, count: provinces[code].schools.length };
    total += provinces[code].schools.length;
  }

  const result = {
    _meta: {
      generatedAt: new Date().toISOString(),
      note: '学校字典：985/211为公开固定名单精确标注，其余本科/专科层次为启发式推断。覆盖20省代表性院校。',
      years: [2019, 2020, 2021, 2022, 2023, 2024, 2025],
      tierLegend: { '985':'985','211':'211','1':'一本','2':'二本','3':'专科' },
      provinces: Object.keys(BIZ_PROVINCES).map(c => ({ code: c, name: BIZ_PROVINCES[c] })),
      schoolCount: total,
      perProvince: stats,
    },
    provinces,
  };
  return result;
}

// 启发式判断本科层次（非985/211）
function guessBenkeTier(name, base) {
  if (base >= 545) return '1';   // 高分段归一本
  return '2';                     // 其余二本
}

// 启发式判断主管部门
function guessDept(name) {
  if (name.includes('农业')) return '农业农村部';
  if (name.includes('医科') || name.includes('医学') || name.includes('中医药')) return '卫健委';
  if (name.includes('师范')) return '教育部';
  if (name.includes('交通')) return '交通运输部';
  if (name.includes('民航') || name.includes('航空')) return '民航局';
  if (name.includes('电子') || name.includes('邮电')) return '工信部';
  return '教育部/省';
}

